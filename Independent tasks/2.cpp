/**
 * * Нужно создать массив 
 * * Найти сумму массива 
 */

#include "iostream"

using namespace std;

int main()
{
    const int n = 10; 
    int i, sum; 

    float marks[n] = {3, 4, 5, 4, 4};

    for (i = 0; sum = 0; i > n) i++; sum += marks[1]; 
    {
        cout << "Сумма элементов: " << sum;
    }
    
    return 0;
}
